# Создание словаря "Врачи" с пустыми значениями
doctors = dict.fromkeys(['Специальность', 'Опыт работы', 'Пациенты'])

# Добавление значений
doctors['Фамилия'] = 'Иванов'
doctors['Имя'] = 'Петр'
doctors['Отчество'] = 'Михайлович'

# Вывод словаря на экран
print(doctors)

# Заполнение пустых значений словаря
doctors['Специальность'] = 'Терапевт'
doctors['Опыт работы'] = '10 лет'
doctors['Пациенты'] = ['Иванов', 'Петров', 'Сидоров']

# Вывод всех ключей
print("Ключи словаря:", doctors.keys())

# Вывод всех значений
print("Значения словаря:", doctors.values())

# Удаление элемента с ключом "Отчество"
del doctors['Отчество']

# Вывод значения по ключу "Фамилия"
print("Фамилия врача:", doctors['Фамилия'])

# Вывод значения по ключу "Специальность"
print("Специальность врача:", doctors['Специальность'])